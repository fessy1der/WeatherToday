                            }
                        }
                }
            } else {
                if locationManager.isLoading {
                    LoadingUIView()
                } else {
                    WelcomeUIView()
                        .environmentObject(locationManager)
                }
            }
        }
        .background(Color(hue: 0.656, saturation: 0.787, brightness: 0.354))
        .preferredColorScheme(.dark)
    }
}

